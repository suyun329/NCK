package user;

public class User {
	private String userName;
	private String userAddress;
	private String userEmail;
	private String userMbank;
	private String userAccount;
	private String userID;
	private String userPw;
	private String userPwc;

	private int userAvailable;

	public int getUserAvailable() {
		return userAvailable;
	}
	public void setUserAvailable(int userAvailable) {
		this.userAvailable = userAvailable;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getUserAddress() {
		return userAddress;
	}
	public void setUserAddress(String userAddress) {
		this.userAddress = userAddress;
	}
	public String getUserEmail() {
		return userEmail;
	}
	public void setUserEmail(String userEmail) {
		this.userEmail = userEmail;
	}
	public String getUserMbank() {
		return userMbank;
	}
	public void setUserMbank(String userMbank) {
		this.userMbank = userMbank;
	}
	public String getUserAccount() {
		return userAccount;
	}
	public void setUserAccount(String userAccount) {
		this.userAccount = userAccount;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getUserPw() {
		return userPw;
	}
	public void setUserPw(String userPw) {
		this.userPw = userPw;
	}
	public String getUserPwc() {
		return userPwc;
	}
	public void setUserPwc(String userPwc) {
		this.userPwc = userPwc;
	}
	
}
